package power;

public class SchemaProperty {

	private String propColumn;
	private String propLabel;
	private String propType;
	public String getPropColumn() {
		return propColumn;
	}
	public void setPropColumn(String propColumn) {
		this.propColumn = propColumn;
	}
	public String getPropLabel() {
		return propLabel;
	}
	public void setPropLabel(String propLabel) {
		this.propLabel = propLabel;
	}
	public String getPropType() {
		return propType;
	}
	public void setPropType(String propType) {
		this.propType = propType;
	}
	
	
}
